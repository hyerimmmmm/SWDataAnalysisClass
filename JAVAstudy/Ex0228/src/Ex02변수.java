
public class Ex02변수 {

	public static void main(String[] args) {
		
		// 정수 형태의 데이터를 담을 수 있는 변수를 선언하겠습니다.
		// 만들어진 변수의 이름을 num이라고 하겠습니다.
		int num;
		
		// num이라는 공간에 데이터 10을 담아주겠습니다.
		num = 10;
		
		// 위의 2줄을 합쳐서 1줄로 만들어주세요.
		int num1 = 10;
		
		System.out.println(num);
		
		// 30을 num이 아닌 새로운 변수에 담아주세요.
		int number = 30;
		
		// number의 값을 100으로 바꿔주세요.
		num = 100;
		
		// 문자열 변수 만들기
		String name;
		
		// 문자열 변수에 값 넣기
		name = "승환";
		
		final int age = 20;
		
		// 논리 타입: boolean(true or false 값만 사용 저장 가능)
		boolean b = false;
		
		// 문자 타입: char
		char c = 'a';
		System.out.println(c);
		
		// 실수 타입: float, double
		double d = 3.14;
		float f = 5.67f;
		
		

	}

}
